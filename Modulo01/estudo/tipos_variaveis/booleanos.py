# Booleanos

if True: 
  print("Vai ser executado")
else:
  print("Não será executado")
  
# Operadores Lógicos: and e or

# AND
if True and True:
  print("Vai ser executado")

if True and False:
  print("Não será executado")
  
if False and False:
  print("Não será executado")
  
# OR
if True or True:
  print("Vai ser executado")
if True or False:
  print("Vai ser executado")
if False or False:
  print("Não será executado")
